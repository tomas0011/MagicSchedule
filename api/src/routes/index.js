const todoPath = require('./path/todo')


//----------------------------------------------------------------------------//
//---------------------------MODULES-IMPORTS----------------------------------//
//-------------------------------API-REST-------------------------------------//
//----------------------------------------------------------------------------//


//----------------------------------------------------------------------------//
//-----------------------MIDDLEWARES-FUNCIONAL--------------------------------//
//----------------------------------------------------------------------------//
const router = Router()

//----------------------------------------------------------------------------//
//-----------------------MIDDLEWARES-ROUTES-----------------------------------//
//----------------------------------------------------------------------------//
router.use('/user', userPath)
router.use('/todo', todoPath)

//--------API-Rest----------//


//-------------------------//

module.exports = router;